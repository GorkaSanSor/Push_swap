#include "libft.h"

char	*ft_strchr(char *str, int c)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == (char)c)
			return (str);
		i++;
	}
	if (c == '\0')
		return (str);
	return (NULL);
}
